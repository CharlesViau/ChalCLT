package Domain.Exporter;

public class Rectangle {

    private double w;
    private double h;
    private double x;
    private double y;

    public Rectangle(double w, double h, double x, double y) {
        this.w = w;
        this.h = h;
        this.x = x;
        this.y = y;
    }

    public double getW() {
        return w;
    }

    public double getH() {
        return h;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}
